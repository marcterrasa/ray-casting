    showTile(screen, w, h, color) {
        screen.drawSquare(new Vector2D(w, h), 1, 1, color);
    }

    mapColor(w, h) {
        switch (this.map[h][w]) {
            case 2:
                return new HSLColor(40); // 'orange';
            case 1:
                return new HSLColor(230); // 'blue;
            case 0:
            default:
                return new HSLColor(0, 0, 0);
        }
    }

    isWall(position) {
        const [w, h] = this.getTileIndex(position);
        return this._isWall(w, h);
    }

    _isWall(w, h) {
        return this.map[h][w] > 0;
    }

    isInsideMap(position) {
        return this._isInsideMap(position.x, position.y);
    }

    _isInsideMap(w, h) {
        return 0 <= w && w < this.width && 0 <= h && h < this.height;
    }

    getTileIndex(mapVector) {
        const flooredVector = Vector2D.floor(mapVector);
        return [flooredVector.x, flooredVector.y];
    }

    calculateWallHits(position, rays) {
        const tileIndex = this.getTileIndex(position);

        const hits = [];
        for (const ray of rays) {
            hits.push(this._calculateWallHit(tileIndex, position, ray));
        }

        return hits;
    }

    calculateWallHit(position, ray) {
        const tileIndex = this.getTileIndex(position);
        return this._calculateWallHit(tileIndex, position, ray);
    }

    _calculateWallHit(mapIndex, mapPosition, ray) {
        let [mapX, mapY] = mapIndex;
        const { x: posX, y: posY } = mapPosition;

        const deltaDistX = Math.abs(1 / ray.x);
        const deltaDistY = Math.abs(1 / ray.y);

        let [stepX, sideDistX] = this.initialSideSetup(mapX, posX, ray.x, deltaDistX);
        let [stepY, sideDistY] = this.initialSideSetup(mapY, posY, ray.y, deltaDistY);

        let hit = false;
        let side;
        let contactPoint;
        while (!hit) {
            if (sideDistX < sideDistY) {
                [contactPoint, sideDistX, mapX] = this.calculateTileHit(mapPosition, ray, sideDistX, deltaDistX, mapX, stepX);
                side = WALL_SIDE.WE;
            } else {
                [contactPoint, sideDistY, mapY] = this.calculateTileHit(mapPosition, ray, sideDistY, deltaDistY, mapY, stepY);
                side = WALL_SIDE.NS; 
            }

            if (!this._isInsideMap(mapX, mapY)) return null;

            hit = this._isWall(mapX, mapY);
        }

        const color = this.mapColor(mapX, mapY);
        return {
            contactPoint: contactPoint,
            side: side,
            color: side === WALL_SIDE.WE ? color.darker(30) : color,
            distance: side === WALL_SIDE.WE ? sideDistX - deltaDistX : sideDistY - deltaDistY
        }
    }

    initialSideSetup(map, pos, ray, deltaDist) {
        return ray < 0 ?
            [-1, (pos - map) * deltaDist] :
            [1, (map + 1.0 - pos) * deltaDist];
    }

    calculateTileHit(mapPosition, ray, sideDist, deltaDist, mapIndex, step) {
        const contactPoint = Vector2D.add(mapPosition, Vector2D.multScalar(ray, sideDist));
        return [
            contactPoint,
            sideDist + deltaDist,
            mapIndex + step,
        ]
    }

    tileCorners(h, w) {
        const x0 = w * this.tileWidth, y0 = h * this.tileHeight;
        const x1 = x0 + this.tileWidth, y1 = y0 + this.tileHeight;
        return [
            new Vector2D(x0, y0),
            new Vector2D(x1, y0),
            new Vector2D(x0, y1),
            new Vector2D(x1, y1),
        ];
    }

    tileSides(h, w) {
        const [c1, c2, c3, c4] = this.tileCorners(h, w);
        return [
            new Segment2D(c1, c2),
            new Segment2D(c2, c4),
            new Segment2D(c4, c3),
            new Segment2D(c3, c1),
        ];
    }


